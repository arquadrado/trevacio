    import { mapGetters, mapActions } from 'vuex'

    export default {
        props: ['response', 'status', 'books'],
        data() {
            return {
                book: null
            } 
        },
        computed: {},
        methods: {
            openBook(book) {
                console.log(book, 'gonna open')
